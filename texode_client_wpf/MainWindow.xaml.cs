    /// <summary>
    /// Interaction logic for MainWindow.xaml
    /// </summary>
    public partial class MainWindow : Window
    {
        public MainWindow()
        {
            InitializeComponent();
        }

        private void ImagePanel_Drop(object sender, DragEventArgs e)
        {
            if (e.Data.GetDataPresent(DataFormats.FileDrop))
            {
                string[] files = (string[])e.Data.GetData(DataFormats.FileDrop);
                NewFileContent = files[0];
            }
        }

        private void ImagePanel_DropForUpdate(object sender, DragEventArgs e)
        {
            if (e.Data.GetDataPresent(DataFormats.FileDrop))
            {
                string[] files = (string[])e.Data.GetData(DataFormats.FileDrop);
                NewFileContentForUpdate = files[0];
            }
        }

        public static string NewFileContent  { get; set; } //SMELT
        public static string NewFileContentForUpdate  { get; set; } //SMELT
    }
}
